<#
.SYNOPSIS
  One-command Graphify refresh: builds a local AST knowledge graph of the
  current project and regenerates the report + interactive visualization.

.DESCRIPTION
  Portable by design - copy this single file into any project (root or a
  scripts/ folder) and run it from there. It only ever touches the
  directory it's invoked from (graphify extract . / graphify cluster-only .).
  No API key, no LLM cost - extraction runs entirely via local tree-sitter
  AST parsing.

  On first run in a project, seeds a starter .graphifyignore (only if one
  doesn't already exist - never overwrites) covering the build-artifact
  directories that most commonly pollute a graph with minified junk.
  Extend it with project-specific paths as needed.

  After building, does a quick sanity check on the God Nodes list: if it's
  full of short/minified-looking names like i() or n(), that's the
  tell-tale sign a build artifact slipped through - the script warns you
  so you can add it to .graphifyignore and re-run.

.PARAMETER Label
  Also name communities with an LLM (costs tokens; needs an API key
  configured for graphify). Default: off (--no-label, fully free).

.PARAMETER NoOpen
  Skip auto-opening graph.html in the default browser when done.

.EXAMPLE
  .\graphify-refresh.ps1
  Rebuilds the graph for the current directory and opens the result.

.EXAMPLE
  .\graphify-refresh.ps1 -NoOpen
  Rebuilds without opening a browser (useful for scripted/CI runs).
#>
[CmdletBinding()]
param(
    [switch]$Label,
    [switch]$NoOpen
)

$ErrorActionPreference = 'Stop'

# ---- Locate graphify: PATH first, then the default uv tool-install location ----
$cmd = Get-Command graphify -ErrorAction SilentlyContinue
if ($cmd) {
    $graphifyExe = $cmd.Source
} else {
    $fallback = Join-Path $env:USERPROFILE '.local\bin\graphify.exe'
    if (Test-Path $fallback) {
        $graphifyExe = $fallback
    } else {
        Write-Error "graphify not found on PATH or at $fallback. Install once with: python -m pip install --user uv ; python -m uv tool install graphifyy"
        exit 1
    }
}

$root = Get-Location
Write-Host "Graphify refresh - $root" -ForegroundColor Cyan

# ---- Seed a starter .graphifyignore on first run only ----
$ignoreFile = Join-Path $root '.graphifyignore'
if (-not (Test-Path $ignoreFile)) {
    $starterIgnore = @'
# Auto-generated by graphify-refresh.ps1 - extend with project-specific paths.
# These are common build-artifact / vendored-code locations that pollute the
# graph with minified junk if left in. Tell-tale sign: God Nodes full of
# short names like i()/n()/t() instead of real function/class names.
dist/
build/
out/
target/
bin/
obj/
coverage/
.next/
.nuxt/
vendor/
android/app/src/main/assets/public/
android/app/build/
ios/App/App/public/
__pycache__/
.venv/
venv/
*.min.js
'@
    $starterIgnore | Out-File -FilePath $ignoreFile -Encoding utf8
    Write-Host "Created starter .graphifyignore - review/extend it if your project has other build outputs." -ForegroundColor Yellow
}

# ---- Extract (local AST, no LLM, no API key) ----
Write-Host ""
Write-Host "[1/2] Extracting (local AST parsing)..." -ForegroundColor Cyan
& $graphifyExe extract . --code-only
if ($LASTEXITCODE -ne 0) { Write-Error "graphify extract failed (exit $LASTEXITCODE)"; exit $LASTEXITCODE }

# ---- Cluster + report + visualization ----
Write-Host ""
Write-Host "[2/2] Building report + visualization..." -ForegroundColor Cyan
if ($Label) {
    & $graphifyExe cluster-only .
} else {
    & $graphifyExe cluster-only . --no-label
}
if ($LASTEXITCODE -ne 0) { Write-Error "graphify cluster-only failed (exit $LASTEXITCODE)"; exit $LASTEXITCODE }

$reportPath = Join-Path $root 'graphify-out\GRAPH_REPORT.md'
$graphPath  = Join-Path $root 'graphify-out\graph.html'

Write-Host ""
Write-Host "Done." -ForegroundColor Green
Write-Host "  Report: $reportPath"
Write-Host "  Graph:  $graphPath"

# ---- Pollution sanity check: warn if God Nodes look minified ----
if (Test-Path $reportPath) {
    $reportText = Get-Content $reportPath -Raw
    if ($reportText -match '(?s)## God Nodes.*?(?=\n## )') {
        $godNodesText = $Matches[0]
        if ($godNodesText -match '`[a-zA-Z]{1,2}\(\)`') {
            Write-Host ""
            Write-Host "WARNING: God Nodes contain short/minified-looking names (e.g. i(), n(), t())." -ForegroundColor Yellow
            Write-Host "  This usually means a build artifact or bundled/minified file got indexed." -ForegroundColor Yellow
            Write-Host "  Add its path to .graphifyignore and re-run this script." -ForegroundColor Yellow
        }
    }
}

if (-not $NoOpen -and (Test-Path $graphPath)) {
    Start-Process $graphPath
}
